package acq;

public interface IOffer {

  /**
   * Get name
   *
   * @return name
   */
  public String getName();

  /**
   * Set name
   *
   * @param name
   */
  public void setName(String name);

  /**
   * Get price
   *
   * @return price
   */
  public int getPrice();

  /**
   * Set price
   *
   * @param price
   */
  public void setPrice(int price);

  /**
   * Get description
   *
   * @return description
   */
  public String getDescription();

  /**
   * Set description
   *
   * @param description
   */
  public void setDescription(String description);
}
